				<h2>Olivia Joslin</h2>
				<h2>Co-Founder</h2>
				<p>Olivia Joslin is the co-founder and CEO of CliqBit, as well as a student at Wellesley College in Wellesley, Massachusetts. She’s originally from Hilton Head Island, South Carolina where she discovered a love for the both environmental studies and computer science.</p>
				<p>Olivia is a decorated researcher; she won awards at the Intel International Science Fair and holds research titles at both the Department of Natural Resources and the National Oceanic & Atmospheric Administration.</p>
				<p>At CliqBit, Olivia enjoys combining her passion for tech and social media. She leads all business operations and is also responsible for directing the app’s user experience and directs the company’s vision. Olivia is confident in the power of a daily laugh.</p>
