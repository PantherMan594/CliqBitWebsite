				<p>I use a lot of social media platforms, but I don’t always enjoy the time I spend on them. Ready to ditch the news and drama of other apps, Hannah and I started CliqBit to switch things up. Growing up, I was always known as the girl who giggled. I think I could find humor in just about anything, and I think CliqBit encourages us all to do the same. On average, everyone has 4-5 good laughs a day and it's time there was a place to share them. I’m looking forward to sharing my funny moments just much as I am to seeing yours! Cheers!</p>
