		<h1 class="centered">Our Story</h1>
		<hr>
		<p class="indent">Did we just get bored during our first year at college? Definitely not. We admittedly stumbled upon this idea while procrastinating studying for finals, but we couldn't give it up.</p>
		<p class="centered">We've been sleep deprived ever since.</p>
		<br>
		<img class="centeredImg" src="assets/img/wellesley.png" />
		<div class="indent" >
			<p>In the winter of 2014, Hannah and Olivia stood at the bottom of a campus hill, doubled over in laughter they recounted the less than graceful moment that had just happened. On her way back from the dining hall, Hannah's feet came straight out from under her, her books flying out of her arms, and she proceeded to slip all the way down an icy hill. The fall was almost something out of a movie.</p>
			<p>To Hannah's surprise, the other witness was one of the only guys on their all-women’s campus — the handsome campus police officer. Of course. Surprise aside, they had one big regret: no one caught it on camera. And with status updates being outdated, there wasn't a place to share the moment with friends.</p>
			<p>This was the beginning of CliqBit.</p>
		</div>
		<p>There wasn't an ideal platform for sharing the brief, and funny moments that make up our lives.</p>
		<p>There are more professional networking sites, which aren't ideal for showcasing our funny sides.</p>
		<p>There are massively popular social networks, but everyone, including Grandma has found her way onto your wall. And Grandma is best not knowing some things.</p>
		<p>There are anonymous networking apps, but let's be honest, it's way more funny when you know who it happened to.</p>
		<p>In the end, there wasn't a platform designed to allow you to be yourself in all your personal, funny, candid glory. Until now.</p>
		<br>
		<p class="centered">CliqBit is the humorous social media platform.</p>
